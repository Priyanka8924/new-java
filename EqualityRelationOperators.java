package com.operators;

public class EqualityRelationOperators {
    public static void main(String[]args) {
    	
    	//Performing the equality and relational operations.
    	int a =100;
    	int b= 200;
    	System.out.println(a>b);
    	System.out.println(a<b);
    	System.out.println(a>=b);
    	System.out.println(a<=b);
    	System.out.println(a!=b);
    }

}
