package com.controlflowstatement;

public class Looping_For_Statement {

	public static void main(String[] args) {
		
		
	//FOR LOOP
		
	     for(int i=333;i<=999;i++)
		{
			 if(i%5==0) {
			 System.out.println(i);
			 }
			
					
		}
	     
	     //when we write print statament in the place of initialization.
	     int f=0;
		for(System.out.println("priyanka"),System.out.println("ram");f<=10;f++)
		{
			System.out.println(f);
		}
		System.out.println("next");
		
		//when we write print statament in the place of increment/decrement.
		
		for(int z=0;z<=5;System.out.println("priyanka"),System.out.println("ram")) {
			System.out.println(z);
			z++;
		}
	}

}
